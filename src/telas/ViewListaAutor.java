    private void imprimirDadosAutorNaLista(ArrayList<Autor> listaAutores) throws Exception {
        DefaultTableModel model = (DefaultTableModel) jTableListaDeAutores.getModel();
        model.setNumRows(0);
        Collections.sort(listaAutores);
        for (int pos = 0; pos < listaAutores.size(); pos++) {
            String[] linha = new String[2];
            Autor aux = listaAutores.get(pos);
            linha[0] = "" + aux.getId();
            linha[1] = aux.getNomeAutor().toUpperCase();
            model.addRow(linha);

        }

    }


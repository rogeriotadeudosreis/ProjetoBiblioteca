     private void imprimirDadosEditoraEmLista(ArrayList<Editora> lista) throws Exception {
        DefaultTableModel model = (DefaultTableModel) jTableListaEditoras.getModel();
        model.setNumRows(0);
        Collections.sort(lista);
        for (int pos = 0; pos < lista.size(); pos++) {
            String[] linha = new String[2];
            Editora aux = lista.get(pos);
            linha[0] = "" + aux.getId();
            linha[1] = aux.getDescricaoEditora().toUpperCase();
            model.addRow(linha);
        }
    }


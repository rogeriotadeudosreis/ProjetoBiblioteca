    private void jButtonPesquisarEditoraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarEditoraActionPerformed
        try {
            imprimirDadosEditoraEmLista(controle.pesquisarEditora(jTextFieldDigitarDados.getText()));
            jTextFieldDigitarDados.requestFocus();
            jTextFieldDigitarDados.setText("");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
            jTextFieldDigitarDados.requestFocus();
            jTextFieldDigitarDados.setText("");
        }
    }//GEN-LAST:event_jButtonPesquisarEditoraActionPerformed

    private void jTextFieldDigitarDadosFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldDigitarDadosFocusGained
        if (jTextFieldDigitarDados.getText().equals("Digite aqui a descrição da editora")) {
            jTextFieldDigitarDados.setText("");
            jButtonPesquisarEditora.setEnabled(true);
        }
    }//GEN-LAST:event_jTextFieldDigitarDadosFocusGained

    private void jTextFieldDigitarDadosFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldDigitarDadosFocusLost
         if (jTextFieldDigitarDados.getText().equals("")) {
            jTextFieldDigitarDados.setText("Digite aqui a descrição da editora");
            jButtonPesquisarEditora.setEnabled(false);
        }
    }//GEN-LAST:event_jTextFieldDigitarDadosFocusLost

